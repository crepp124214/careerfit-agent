"""项目常量定义模块

集中管理所有硬编码的配置值，便于维护和调整。
"""
from __future__ import annotations


# ============================================================================
# RAG 检索配置
# ============================================================================

class RAGConfig:
    """RAG 检索相关配置"""
    
    # 默认检索结果数量
    DEFAULT_TOP_K: int = 3
    
    # 相似度阈值
    SIMILARITY_THRESHOLD: float = 0.0
    
    # 最大检索文档数（防止性能问题）
    MAX_RETRIEVAL_DOCUMENTS: int = 1000
    
    # 向量维度
    VECTOR_DIMENSION: int = 768


# ============================================================================
# LLM 配置
# ============================================================================

class LLMConfig:
    """LLM 相关配置"""
    
    # 默认超时时间（秒）
    DEFAULT_TIMEOUT_SECONDS: int = 120
    
    # 最大重试次数
    MAX_RETRIES: int = 3
    
    # 重试间隔（秒）
    RETRY_DELAY_SECONDS: float = 1.0
    
    # 缓存 TTL（秒）
    CACHE_TTL_SECONDS: int = 3600
    
    # 缓存最大条目数
    CACHE_MAX_SIZE: int = 1000


# ============================================================================
# 分析任务配置
# ============================================================================

class AnalysisConfig:
    """分析任务相关配置"""
    
    # 后台任务启动延迟（秒）
    BACKGROUND_TASK_DELAY_SECONDS: float = 0.5
    
    # 事件回放缓存 TTL（秒）
    EVENT_CACHE_TTL_SECONDS: int = 600
    
    # 事件回放缓存最大条目数
    EVENT_CACHE_MAX_SIZE: int = 500
    
    # 工作流节点总数（用于进度计算）
    WORKFLOW_TOTAL_NODES: int = 7


# ============================================================================
# 数据库配置
# ============================================================================

class DatabaseConfig:
    """数据库相关配置"""
    
    # 连接池大小
    POOL_SIZE: int = 20
    
    # 最大溢出连接数
    MAX_OVERFLOW: int = 30
    
    # 连接回收时间（秒）
    POOL_RECYCLE_SECONDS: int = 3600
    
    # 连接前 ping 检查
    POOL_PRE_PING: bool = True


# ============================================================================
# 安全配置
# ============================================================================

class SecurityConfig:
    """安全相关配置"""
    
    # 日志中显示的最大文本长度
    LOG_MAX_TEXT_LENGTH: int = 50
    
    # 文本预览长度
    TEXT_PREVIEW_LENGTH: int = 20
    
    # 正则表达式最大长度
    MAX_PATTERN_LENGTH: int = 1000
    
    # 请求频率限制（每分钟）
    RATE_LIMIT_PER_MINUTE: int = 60


# ============================================================================
# 评分配置
# ============================================================================

class ScoringConfig:
    """评分相关配置"""
    
    # 评分版本
    SCORING_VERSION: str = "2.0"
    
    # 默认分数
    DEFAULT_SCORE: int = 0
    
    # 分数范围
    MIN_SCORE: int = 0
    MAX_SCORE: int = 100


# ============================================================================
# 文件上传配置
# ============================================================================

class UploadConfig:
    """文件上传相关配置"""
    
    # 最大文件大小（字节）
    MAX_FILE_SIZE: int = 10 * 1024 * 1024  # 10MB
    
    # 允许的文件类型
    ALLOWED_FILE_TYPES: list[str] = [".pdf", ".doc", ".docx", ".txt"]
    
    # 上传文件保存路径
    UPLOAD_DIR: str = "uploads"
